# Operadores relacionales
# >  <  != ==  <=   >=
num1 = 23
num2 = 23
num2 = 2

print("Num1 es mayor? ", num1 > num2)              # True
print("Num1 es menor? ",num1 < num2)               # False
num2 = 9
print("Num1 es mayor o igual? ",num1 >= num2)      # True
print("Num1 es menor o igual? ",num1 <= num2)      # False
print("Num1 es igual a num2? ",num1 == num2)        # False
print("Num1 es diferente? ",num1 != num2)          # True

